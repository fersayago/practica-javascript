
// CICLO WHILE

let variableWhile = 0;

while (variableWhile < 10) {
  console.log(`variable While: ${variableWhile}`);
  variableWhile++;
}

// CICLO DO WHILE

let variableDoWhile = 0;

do {
  console.log(`variable DoWhile: ${variableDoWhile}`);
  variableDoWhile++;
} while (variableDoWhile < 10);


// CICLO FOR

for (let variableFor = 0; variableFor < 10; variableFor++) {
  console.log(`variable For:${variableFor}`);
}